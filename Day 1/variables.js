//Direct variable
x = 10;

//Variable with var keyword
var y = 20;

//Variable with let keyword
let z = 30; 

//Variable with const keyword
const pi = 3.14;

console.log("Direct variable x:", x);
console.log("Variable y with var keyword:", y);
console.log("Variable z with let keyword:", z);
console.log("Constant pi with const keyword:", pi);