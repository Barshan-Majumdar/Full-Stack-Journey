// 1. Number
let integerNum = 42;
let floatNum = 3.14;
console.log("Integer Number:", integerNum);
console.log("Floating Point Number:", floatNum);

// 2. String
let singleQuoteStr = 'Hello, World!';
let doubleQuoteStr = "JavaScript is fun!";
console.log("Single Quote String:", singleQuoteStr);
console.log("Double Quote String:", doubleQuoteStr);

// 3. Boolean
let isJavaScriptFun = true;
let isSkyGreen = false;
console.log("Is JavaScript fun?", isJavaScriptFun);
console.log("Is the sky green?", isSkyGreen);

// 4. Undefined
let undefinedVar;
console.log("Undefined Variable:", undefinedVar);

// 5. Null
let nullVar = null;
console.log("Null Variable:", nullVar);

// 6. Object
let person = {
    name: "Alice",
    age: 25
};
console.log("Object Person:", person);

// 7. Array
let colors = ["Red", "Green", "Blue"];
console.log("Array of Colors:", colors);   

// 8. Symbol
let sym1 = Symbol('uniqueIdentifier');
let sym2 = Symbol('uniqueIdentifier');
console.log("Are sym1 and sym2 equal?", sym1 === sym2);

// 9. BigInt
let bigIntNum = BigInt(9007199254740991);
console.log("BigInt Number:", bigIntNum);

