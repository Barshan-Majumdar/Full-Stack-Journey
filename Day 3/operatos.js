//multipe operators in javascript

let a = 10;
let b = 5;
let c = 2;

// arithmetic operators
//1. Addition
let sum = a + b + c;

//2. Subtraction
let difference = a - b - c;

//3. Multiplication
let product = a * b * c;

//4. Division
let quotient = a / b / c;

//5. Modulus
let remainder = a % b % c;

//6. Exponentiation
let power = a ** b ** c;


// Comparison operators
let isEqual = (a == b);
let isNotEqual = (a != b);  
let isGreater = (a > b);
let isLess = (a < b);
let isGreaterOrEqual = (a >= b);
let isLessOrEqual = (a <= b);

// Logical operators
let andOperator = (a > b) && (b > c);
let orOperator = (a > b) || (b < c);
let notOperator = !(a > b);

// Assignment operators
let x = 10;
x += 5;  // x = x + 5
x -= 3;  // x = x - 3
x *= 2;  // x = x * 2
x /= 4;  // x = x / 4
x %= 3;  // x = x % 3
x **= 2; // x = x ** 2 

// Increment and Decrement operators
let y = 10;
y++;  // y = y + 1
y--;  // y = y - 1  
++y;  // Pre-increment
--y;  // Pre-decrement

